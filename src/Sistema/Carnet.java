    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        int resultado ;

        carnett Buscador = new carnett();

        FileNameExtensionFilter formato = new FileNameExtensionFilter ("JPG, PNG Y GIF","jpg","png","gif");

        Buscador.JFCIMG.setFileFilter(formato);

        resultado = Buscador.JFCIMG.showOpenDialog(null);

        if(JFileChooser.APPROVE_OPTION== resultado){

            archivo=Buscador.JFCIMG.getSelectedFile();
            //Jdireccion.setText(archivo.getAbsoluteFile());

            try {
                ImageIcon ImgIcon = new ImageIcon (archivo.toString());

                Icon icono=new ImageIcon(ImgIcon.getImage().getScaledInstance(jfoto.getWidth(),jfoto.getHeight(), Image.SCALE_AREA_AVERAGING));

                jfoto.setIcon(icono);

            }catch (Exception ex)
            {
                JOptionPane.showMessageDialog(null,"Error al abrir"+ ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

