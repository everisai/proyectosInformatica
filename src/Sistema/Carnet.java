    private void IMPRIME(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IMPRIME
        // TODO add your handling code here:
      try 
      {
          PrinterJob job =PrinterJob.getPrinterJob();
          job.setPrintable(this);
        boolean ok = job.printDialog();
        
        if(ok)
        {
         job.print();
              this.dispose();
        
        }
        Registro verregistro = new Registro();
        verregistro.setVisible(true);
        this.dispose();
        
          
      } catch (PrinterException ex) {
            JOptionPane.showMessageDialog(null, "Error de programa","Error\n"+ex,JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_IMPRIME
    
        
    
